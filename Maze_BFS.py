def search(x,y):
    frontier.append((x, y))
    solution[x,y] = x,y

    while len(frontier) > 0:          # exit while loop when frontier queue equals zero
        time.sleep(0)
        x, y = frontier.popleft()     # pop next entry in the frontier queue an assign to x and y location

        if(x - 10, y) in path and (x - 10, y) not in visited:  # check the cell on the left
            cell = (x - 10, y)
            solution[cell] = x, y    # backtracking routine [cell] is the previous cell. x, y is the current cell
            blue.goto(cell)        # identify frontier cells
            blue.stamp()
            frontier.append(cell)   # add cell to frontier list
            visited.add((x-10, y))  # add cell to visited list

        if (x, y - 10) in path and (x, y - 10) not in visited:  # check the cell down
            cell = (x, y - 10)
            solution[cell] = x, y
            blue.goto(cell)
            blue.stamp()
            frontier.append(cell)
            visited.add((x, y - 10))
